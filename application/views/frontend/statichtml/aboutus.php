                        <h1>About Us</h1>
                        <p>Headquartered in Scottsdale, Arizona, Karmora is the collaborative creation of a seasoned sales professional with more than 30 years of retail experience and a group of the brightest physicians, aestheticians, web developers, programmers and online marketing experts across the globe. </p>

                        <p>Libby McPhillips, a mother of two and grandmother to five, was still working retail in a prominent Scottsdale mall in 2012 when she realized that increasing numbers of her regular customers were electing to shop online instead of in the store by way of the merchandise they would return.  Seeing the paradigm shift in shopping habits, and tired of being on her feet 8 hours a day for nearly 30 years, Libby reached out to a close family member about creating a website where she could leverage her retail experience and replace her income from the comfort of her home. </p>

                        <p>After 30 years of retail, the reality was that Libby didn’t even have the investment capital required to fund a website, so she took her idea to her son-in-law, Mike Maksudian, who is owner of a Scottsdale based IT sales and consulting organization, and asked for his help.  Seeing Libby’s vision and determination, Mike agreed to fund the website and began assembling a team of the brightest website developers, programmers, and coders to build the face of Libby’s dream from the ground up with the single purpose of creating the most comprehensive, user friendly, social media-centric, shopping portal for home based business the internet has ever seen.  It took more than two years, and an incredible amount of patience, dedication and resources, but it was worth it!  Today, your Karmora website has over 2,000 pages off content designed specifically to make you happier, healthier and wealthier!</p>

                        <p>With the website puzzle solved, Libby turned her attention to being able to deliver all natural, toxic free, products to her Shoppers.  Libby teamed up with close friends of the family, Dr. Steven Locnikar, DO (Arizona Board Certified Plastic Surgeon) and his wife Kyra Locnikar (Arizona Board Certified Aesthetician), and began developing high quality, all natural, ToxicFree™ skin care products, vitamins, dietary supplements and weight loss aids.  With loving care, Libby and the Locnikars continue to research and develop high quality products for our Shoppers.  In fact, Karmora is one of the few companies in the world to be granted the certified ToxicFree™ designation for its Flawless Skincare line and offers more than a dozen all natural dietary supplements and weight loss aids.</p>

                        <p>Karmora started with just a few online retailers in 2013 and has grown into an exciting home based business offering over 2,000 retail stores and high quality Exclusive Products designed specifically to help our Shoppers become Happier, Healthier and Wealthier!    From our certified ToxicFree™ Flawless Skin Care products… to our all natural Karmora Exclusive weight loss and dietary supplements; From our handpicked and hand crafted clothing, jewelry and accessories… to our Smokin Hot Deals; you can be certain that every Exclusive Product is of the highest quality and is being consumed, worn or used by our own families!</p>

                        <p>Libby’s dream was to create an opportunity that anyone, at any age, with even the slightest bit of internet experience could build their very own Shopping Community where they could save money, make money offering quality products, and have fun doing it. Every day our company strives to help each and every one of our Shoppers become Happier, Healthier and Wealthier!  And, we are happy to say that today... Libby’s dream is a reality!</p>

